                            <p>Nestled in between Washington state and the Canadian island of Vancouver, the San Juan Islands are an incredible vacation hub, just two hours north of Seattle. The largest of these islands is Orcas, a nearly 60 square mile island that features everything from rugged waterfall hikes, and excellent kayaking to premium restaurant experiences</p>
                            <p>
                                <a href="javascript:void(0);"> <img src="<?php echo get_template_directory_uri(); ?>/images/single_1.jpg" alt="post_1" class="img-fluid"></a>
                            </p>
                            <p>The best time to explore Orcas is in the summer! The weather can be anything from chilly and a little bit damp to high nineties heat starting in May. This makes July and August busy months for this small island, with September being a little less hectic.</p>
                            <p>Getting to Orcas from Seattle is, as the Orcas Island Chamber of Commerce says, ‘half the fun’! There is not, unfortunately, a ferry from Seattle to Orcas. Instead, you either need to fly directly to Orcas or drive to Anacortes where you can catch a ferry to take you to the Island.</p>
                            <p>You can fly directly to Orcas Island via Kenmore Air. There are many different locations you can fly out of including from SeaTac (you can find all the options for flying to and from Orcas on their website).</p>
                            <p>To travel by car it is about a two-hour journey heading north on I-5. Once you reach Anacortes you’ll drive to the ferry terminal and board a ferry to take you to Orcas!</p>
                            <h3>Orcas Island Ferry
                            </h3>
                            <p>The San Juan Ferry Terminal is located in quaint Anacortes. As you drive through this little town, there are signs directing you through the downtown and out away from the town to the ferry landing (about fifteen minutes from Anacortes to the ferry terminal).</p>
                            <p>It takes about an hour to an hour and a half for most ferry rides, depending on how many stops the ferry is scheduled to make. However you will need to arrive a half hour early if you’ve made a reservation for the ferry, and up to two hours early if you haven’t made a reservation.</p>
                            <p>The ride is a lovely sail through the islands. Keep your eyes peeled for eagles in the treetops and seals on the rocks! When I ride the ferry, I always get some chowder and a cider from the cafeteria and enjoy the views.</p>
                            <p>
                                <a href="javascript:void(0);"><img src="<?php echo get_template_directory_uri(); ?>/images/single_2.jpg" alt="single_2" class="img-fluid"></a>
                            </p>
                            <h3>Parking at the Ferry Landings
                            </h3>
                            <p>Parking at Anacortes is available in any of their four parking lots. Simply pick a parking spot, find the parking spot number and pay at the lots pay station. There is also a pay by phone option you can explore with the signs in the lots.</p>
                            <p>The parking at the Orcas Island ferry terminal is a little bit more difficult. There are about fifty-two spaces available for parking but these spaces fill up quick in the summer! There is no additional parking along the country road.  </p>
                            <p>If you’re hungry at the Orcas ferry landing, there is the Orcas Hotel cafe Octavia’s, that offers selections of local seafood and pasta. You’ll find it in the white building next to the park and restrooms on the hillside at the ferry terminal.</p>
                            <div class="single__view">
                                <a href="javascript:void(0);"><img src="<?php echo get_template_directory_uri(); ?>/images/single_3.jpg" alt="single_3" class="img-fluid"></a>
                                <div class="single__text">Orcas Hotel</div>
                            </div>
                            <p>But if you’re looking for some coffee and snacks for the ferry, you can find the Orcas Village Store, a cozy market right on the water and next to the ferry dock. The store is complete with deli and bakery and a fine liquor selection.</p>
                            <div class="single__view">
                                <a href="javascript:void(0);"><img src="<?php echo get_template_directory_uri(); ?>/images/single_4.jpg" alt="single_4" class="img-fluid"></a>
                                <div class="single__text">Orcas Village Store</div>
                            </div>
                            <p>Now walking from the ferry into Eastsound may sound like a nice stroll, but Eastsound is, in fact, a little less than nine miles away from the ferry terminal! There has been more than one time I’ve picked up hitchhikers who thought they could make it from the ferry dock into town! So I would definitely call ahead for a taxi, or rent a car!
                            </p>
                            <h3>Orcas Island Ferry Reservations</h3>
                            <p>The cost of ferry travel varies for your age and the type of vehicle you’re driving. But if you’re driving a standard car and are over the age of nineteen your total is going to come to fifty-five dollars. It’s an additional thirteen dollars for every passenger in the car.</p>
                            <p>Booking can be really easy if you can figure out the ferry website! I personally use the ferry app because I’m always making reservations on the go! But on the main website simply go to the make a reservation page and fill out the information about what days you’re planning to travel and what type of car you are driving. It will then show you available sailings. Select one of these and fill out the payment options! I recommend making these reservations as soon as you know you are going, especially in the summer, as the sailings will sell out very quickly!</p>
                            <p>You can, of course, book your sailing at the ferry terminal without a reservation, but you’d be risking not catching a ferry!  You can also see how many spots are left on the ferry if you’re in a rush on the ferry app.</p>
                            <p>There is a ferry that will take you to Friday Harbor on San Juan island from Orcas if you are looking to expand your San Juan experience. Friday Harbor offers a bigger town feel with the same beautiful island views. Also from Friday Harbor, you can sail to Sidney B.C.</p>
